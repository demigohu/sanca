fn fp_bytes_gt(a: &[u8; 48], b: &[u8; 48]) -> bool {
    for (left, right) in a.iter().zip(b.iter()) {
        if left != right {
            return *left > *right;
        }
    }
    false
}

/// Re-encode an uncompressed G1 point to IETF compressed form (drand API format).
fn g1_compressed_from_uncompressed(uncompressed: &[u8; 96]) -> [u8; 48] {
    let mut compressed = [0u8; 48];
    compressed.copy_from_slice(&uncompressed[0..48]);
    compressed[0] |= 0x80;
    let y: [u8; 48] = uncompressed[48..96].try_into().unwrap();
    if fp_bytes_gt(&y, &FP_LEX_HALF) {
        compressed[0] |= 0x20;
    }
    compressed
}

/// Ensure the drand API signature matches the uncompressed point used for BLS verification.
fn g1_compressed_matches(uncompressed: &[u8; 96], compressed: &[u8; 48]) -> bool {
    g1_compressed_from_uncompressed(uncompressed) == *compressed
}

/// Build the drand unchained beacon message: SHA-256(round_be_bytes).
fn drand_beacon_message(env: &Env, round: u64) -> Bytes {
    let round_bytes = Bytes::from_array(env, &round.to_be_bytes());
    let hash = env.crypto().sha256(&round_bytes);
    Bytes::from_array(env, &hash.to_array())
}

/// Verify a drand quicknet (`bls-unchained-g1-rfc9380`) beacon and return
/// `SHA-256(compressed_signature)` as the shuffle seed.
pub fn verify_quicknet_beacon(
    env: &Env,
    drand_public_key: &BytesN<192>,
    round: u64,
    signature_uncompressed: &Bytes,
    signature_compressed: &Bytes,
) -> Result<Bytes, Error> {
    if signature_uncompressed.len() != 96 {
        return Err(Error::InvalidDrandSignature);
    }
    if signature_compressed.len() != 48 {
        return Err(Error::InvalidDrandSignature);
    }

    let mut sig_arr = [0u8; 96];
    signature_uncompressed.copy_into_slice(&mut sig_arr);
    let mut compressed_arr = [0u8; 48];
    signature_compressed.copy_into_slice(&mut compressed_arr);
    if !g1_compressed_matches(&sig_arr, &compressed_arr) {
        return Err(Error::InvalidDrandSignature);
    }

    let expected_randomness = env.crypto().sha256(signature_compressed);
    let bls: Bls12_381 = env.crypto().bls12_381();

    let signature = G1Affine::from_bytes(BytesN::from_array(env, &sig_arr));
    if !bls.g1_is_in_subgroup(&signature) {
        return Err(Error::InvalidDrandSignature);
    }

    let public_key = G2Affine::from_bytes(drand_public_key.clone());
    if !bls.g2_is_in_subgroup(&public_key) {
        return Err(Error::InvalidDrandSignature);
    }

    let message = drand_beacon_message(env, round);
    let dst = Bytes::from_slice(env, DRAND_HASH_TO_G1_DST);
    let message_on_g1 = bls.hash_to_g1(&message, &dst);

    let generator = G2Affine::from_bytes(BytesN::from_array(env, &G2_GENERATOR));
    let neg_signature = -signature;

    // e(-signature, G2) * e(H(message), public_key) == 1  <=>  e(signature, G2) == e(H(message), public_key)
    let vp1 = Vec::from_array(env, [neg_signature, message_on_g1]);
    let vp2 = Vec::from_array(env, [generator, public_key]);
    if !bls.pairing_check(vp1, vp2) {
        return Err(Error::InvalidDrandSignature);
    }

    Ok(Bytes::from_array(env, &expected_randomness.to_array()))
}
